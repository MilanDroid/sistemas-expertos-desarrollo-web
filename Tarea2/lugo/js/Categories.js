const categoriesCards = ((list, element) => {
    let cards = ``;
    list.forEach((category, key) => {
        const businessCount = category.empresas.length; 
        cards += `<div class="col-sm-12 col-md-6 col-lg-4 col-xl-3">
            <div class="card-rounded my-3 mx-3 px-3" id="cards-container"
            style="background-color: ${category.color};"
            onclick="categoryDetail('detail-title', 'detail-body',${key})"
            data-toggle="modal" data-target="#details-modal">
                <div class="py-4">
                    <i class="${category.icono} card-icon text-white"></i>
                </div>
                <div><h2 class="text-white font-weight-bolder mt-4">${category.nombreCategoria}</h2></div>
                <div><h4 class="text-white font-weight-light pb-3">${businessCount} Comercios</h4></div>
            </div>
        </div>`;
    });

    element.innerHTML = cards;
});

const showCategoryDetail = ((titleElement, bodyElement, categoryId) => {
    const modalTitle = document.getElementById(titleElement);
    const modalBody = document.getElementById(bodyElement);
    const category = categoriesStorage[categoryId];

    modalBody.innerHTML = "";
    modalTitle.innerHTML = "";

    let bodyContent = `<div class="row">`;
    category.empresas.forEach((empresa) => {
        bodyContent += `<div class="col-xl-6 col-lg-6 col-md-12 col-sm-12">
        <div class="card card-rounded m-3">
        <img class="card-img-top card-rounded-top w-100" src="${empresa.imagen}" srcset="${empresa.imagen}" alt="${empresa.nombreEmpresa}">
        <div class="card-body">
        <h1 class="card-title font-weight-bold text-white " style="margin-top: -6rem;">${empresa.nombreEmpresa}</h1>`;

        empresa.productos.forEach((producto, key) => {
            bodyContent += `<div class="row mt-5 mb-3">
                <div class="col-6">
                    <h3 class="text-lugo font-weight-normal">${producto.nombreProducto}</h3>
                </div>
                <div class="col-6 text-right">
                    <button type="button" class="btn btn-primary-dark bg-dark-lugo rounded-pill text-white px-5"
                    onclick="addOrder('${producto.nombreProducto}', '${producto.descripcion}', ${producto.precio})"
                    data-toggle="modal" data-target="#add-order-modal">Pedir</button>
                </div>
            </div>
            <p class="card-text">${producto.descripcion}</p>
            <p class="card-text text-right">
                <span class="text-secondary ml-auto"><b>$${producto.precio}</b></span>
            </p>`;

            if ( empresa.productos.length > (key+1) ) {
                bodyContent += '<div class="my-3"><hr></div>';
            }
        });
        bodyContent += `</div></div></div>`;
    });
    bodyContent += `</div>`;

    modalTitle.innerHTML = `${category.nombreCategoria}`;
    modalBody.innerHTML = bodyContent;
});
export {categories, categoriesCards, showCategoryDetail};